package com.india.tamilnadu.tally.vo;

public class StockStatistics {

	private String stockWeek;
	private String stockMonth;
	private String stockQuarter;
	private String stockYear;
	public String getStockWeek() {
		return stockWeek;
	}
	public void setStockWeek(String stockWeek) {
		this.stockWeek = stockWeek;
	}
	public String getStockMonth() {
		return stockMonth;
	}
	public void setStockMonth(String stockMonth) {
		this.stockMonth = stockMonth;
	}
	public String getStockQuarter() {
		return stockQuarter;
	}
	public void setStockQuarter(String stockQuarter) {
		this.stockQuarter = stockQuarter;
	}
	public String getStockYear() {
		return stockYear;
	}
	public void setStockYear(String stockYear) {
		this.stockYear = stockYear;
	}
	
	
}
